class Wado < Formula
  desc "Command line tool to manage WildFly containers"
  homepage "https://github.com/hpehl/wado"
  url "https://github.com/hpehl/wado/releases/download/v0.2.6/wado-x86_64-apple-darwin.tar.gz"
  sha256 "deae76df5e72c47cb9fc278550cb8cd0ed2c54b2cc8419f6067aca930f8415f6"
  version "0.2.6"
  license "Apache-2.0"

  def install
    bin.install "wado"
    bash_completion.install "wado.bash" => "wado"
    zsh_completion.install "_wado"
    fish_completion.install "wado.fish"
  end

  test do
      system "#{bin}/wado", "--version"
    end
end
